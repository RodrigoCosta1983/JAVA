public class FluxoWhile {
    public static void main(String[] args) {
        
        int i = 0;
        while (i < 2) {                // Loop n vezes !!!
           // System.out.println(i);
            i++;

        }

        int x = 3;
        do {                           // Apenas um Loop !!!
            System.out.println(x);
            i++;
        } while (i < 2);
        
    }
}
